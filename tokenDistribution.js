async function distributeTokens(
  providerUrl,
  privateKey,
  tokenAddress,
  addresses
) {
  const provider = new ethers.JsonRpcProvider(providerUrl);
  const wallet = new ethers.Wallet(privateKey, provider);
  const token = new ethers.Contract(tokenAddress, abi, wallet);

  for (const address of addresses) {
    const amount = Math.floor(Math.random() * 999) + 1;
    try {
      const gasPrice = ethers.parseUnits("50", "gwei");
      const tx = await token.transfer(
        address,
        ethers.parseUnits(amount.toString(), 18, { gasPrice })
      );
      await tx.wait();
      console.log(`Sent ${amount} tokens to ${address}`);
    } catch (error) {
      console.error(`Error sending tokens to ${address}: ${error.message}`);
    }
  }
}

